module.exports = function convert(title) {
  switch (title) {
    case 'vinland': 
      return 'vinland-saga';
    case 'fire':
      return 'fire-force';
    case 'stone':
      return 'dr-stone';
    case 'boku':
      return 'boku-no-hero-academia-s4';
    case 'nanatsu':
      return 'nanatsu-no-taizai-s3';
    }
}

